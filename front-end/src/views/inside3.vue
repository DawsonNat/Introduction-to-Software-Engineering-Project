            </p>
            <p>
                【编故事】：编故事的时候，要把单词的中文意思（犹豫）和这三个“小单词”（he、sit、ate）的中文意思组合在一起，从而编成一个小故事。比如，我们可以这样编：hesitate——他坐着吃鱿鱼（犹豫）。
            </p>
            <p>
                【想象画面】：编完之后，在大脑里面把这个场景想象一下，想象一个人正坐在地上，一口一口地在吃鱿鱼，这么深刻的画面，一下子就把单词牢牢记住了。
            </p>
            <div class="subtitle"><h3 class="keyPoint">5、谐音记忆法</h3></div>
            <p>
                【此法原理】：谐音法（The homophonic memory）就是按照单词的读音来进行编码，将读音换成我们形象的中文意思。这种方法我们应该很熟悉，小时候刚接触英文的时候，我们用的就是这种办法，不会读，只能用中文意思把读音翻译出来。
            </p>
            <p>
                【此法优点】：这种方法<span class="highlight">幽默风趣，让人过目不忘。</span>例如 thank you，谐音法可翻译为“三克油”，记忆的时候记为“谢谢你给了我三克油”。
            </p>
            <p>
                【此法缺点】：一是如果对单词<span class="highlight">发音的规则掌握得不好</span>的话，即使知道怎么读，却不一定能够按照发音规则写得出相应的字母。二是如果没有掌握好正确的单词发音，就用谐音法来代替标准的发音，这对于英语口语来说是一个非常不好的习惯。
            </p>
