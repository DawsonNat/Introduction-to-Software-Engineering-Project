            </p>
            <div class="subtitle"><h3 class="keyPoint">（一）不要用中文来理解听力</h3></div>
            <p>
                英语听力是一件完全用不到中文的过程，我们要听到的是<span class="highlight2">英语本身</span>，而非一定要翻译成中文记忆。

                很多人认为，练习听力和阅读的本质是一样的。对于初学者，<span class="highlight2">只能通过中文来理解英语，实际上这是一个误区。</span>
            </p>
            <p>
                大家总觉得当自己对听力足够熟悉的时候，你才有资格摒弃中文意思，单纯的听英语，所以我真的看到很多同学练习听力的时候会一点一点的把每一句话的中文翻译写上，很想对同学们说，我们是在<span class="highlight2">练习听力，而不是翻译作文鸭！</span>
            </p>
            <p>
                想要随心所欲地像native speakers一样听英语，再也<span class="highlight2">不受中文的干扰吗？</span>
            </p>
            <p>
                对于初学英语的人，由于对单词的意思没有形成本能的反馈，就会自然而然地将每个单词的意思在脑海中转换成中文，这时大脑会本能地拼凑出整个句子的意思。
            </p>
            <p>
                这个拼凑的过程是由大脑自动完成的，是无意识地。由于这过程中间有中文作为媒介，所以我认为这就是在用中文来理解，这是很正常的处理过程。所以，千万不要把用中文理解等同于用中文一句句地主动翻译。
            </p>
            <p>
                <span class="highlight2">用中文理解是一个我们的本能反应也是必经的阶段，</span>它本身并不是目的。真正目的是抛弃中文，最终达到随心所欲直接理解英文的水平。而务必要<span>避免用中文一句句地主动翻译</span>，你未免太低估了自己的大脑，因为这完全是毫无必要的。
            </p>
            <p>
                所以这里面我说的摒弃中文理解，是这个意思，大家明确了吧！

                那么听力应该怎样稳步提升，让大家到达可以随心所听懂 native speaker 的程度呢？

                下面给大家推荐几个听力的有效小技巧和小窍门！
            </p>    
            <div class="subtitle"><h3 class="keyPoint">（二）高效精听</h3></div>
            <p>
                之前看到过一句话，说如果你坚持做一件事三个月，那么带来的效果是非常显著的。但是大部分人每次在做一件事时，坚持了不到一个月甚至不到一个星期，就想着快点见成效。对于英语的学习固然如此，大部分人坚持不了而半途而废。
            </p>
            <p>
                很多同学容易在最开始的初期，还没有系统的训练和练习，就在心里否定自己，给自己负面暗示。或者觉得持之以恒每天坚持训练这件事根本就做不成，或者这种方法根本就没用，一点也不适合自己。
            </p>
            <p>
                但是楼主要坚定的告诉大家，不单是你们，我在在练听力的时候，也无数次的这样想过，甚至有时候听的怀疑人生，<span class="highlight2">但每次都告诉自己一定要坚持下去，最后才有了显著的效果！</span>
            </p>
            <div class="subtitle"><h3 class="keyPoint">1、精听素材的选择</h3></div>
            <p>
                （1）文章不宜过长
            </p>
            <p>
                一篇好的精听素材，千万不要太长，尤其是对于听力初听者， 200 到 300 个单词为最佳。因为一篇长文章会给自己带来“累赘冗长”的感觉，在还没有开始听的时候，心理负担就已经加重，不利于后面的听写。尤其是在听的过程中，听了很久，这篇文章还是没有结束。心里就会产生厌倦，甚至导致放弃听力。
            </p>
            <p>
                （2）生词不宜过多
            </p>
            <p>
                精听素材的选择，还有很重要的一点就是，生词不宜过多。所谓生词不宜过多，指的是整篇文章在听之前，自己先预览一遍，文中大部分单词自己一定要认得，只有少数单词可以不认识。因为听力中涉及了很多连读，尤其是当语速快的时候，连读是一定有的。
            </p>
            <p>
                所以在选择精听素材时，生词一定不要过多，否则就是对自己的折磨。当然也不要太简单的单词和句子，和自己水平相当的就可以了。
            </p>
            <img src="../assets/image/四级1图片/p4.webp" alt="">
            <p>
                （3） 语速适当加快
            </p>
            <p>
                很多同学们在练习听力时有一个误区，就是自己英语一般，听力很烂，所以刚开始选的听力语速都偏慢。对于听力素材的选择，语速不要过慢，和英语基础完全没有关系，只要记得前两个原则！并且一定要遵循前<span class="highlight2">两个原则</span>，就是<span class="highlight2">文章不要过长，生词不要过多</span>，在这两个基础上选怎样语速的都没关系！
            </p>
            <p>
                【原因】：当文章不长，生词不多时，这个时候我们听的就是<span class="highlight2">单词的发音与连读。</span>在这个过程中，你会发现自己有些单词之前读错了，或者有些短语和词组原来可以这样连着读。这就是经验。
            </p>
            <p>
                当你听多了之后，自己就有感觉哪些词它会连着读，哪些词的某些音在口语中不会发，即使音标有这个音。当这样的听力累积起来后，某一天你一定会突然发现自己的英语好像真的上了一个台阶。因为这不仅是对听力的练习，也是对单词和英语口语的练习。这个时候你就可以适当听一些稍微长一点（当然还是不要太长）、生词多一点点、语速偏快一点的文章了。
            </p>
            <p>
                其实，精听还是在坚持，知道大家每天课程忙碌，有时课多到无法每天都听，但每天都至少听点东西，多多少少会有提升，课少的时候建议大家静下心来精听 BBC ，像我朋友的话，她练习精听以后的最直观感受就是：<span class="highlight2">从以前的一篇要花两小时，错误率巨高，到现在半小时，错很少。这些有目共睹的成长呀。</span>
            </p>
            <div class="subtitle"><h3 class="keyPoint">（三）高效泛听</h3></div>
            <p>
                很多同学也都知道泛听，但不是很了解什么是泛听，泛听与精听相对，只要不影响对文章的整体理解，一个词、一个短语甚至是一个句子听不懂没关系，目的是让听者在练习中掌握文章的整体大意和结构。
            </p>
            <p>
                当然，泛听并非指的什么材料都听。大家可能平时有收听新闻的习惯，但如果自己只能听懂60 % 以下，或纯粹当作背景音，那这样的泛听除了有点心理安慰，也就没有其它大作用了。
            </p>
            <p>
                所以即使是泛听，也要先了解自己的水平，去找不同难度和题材的听力材料进行试验。很多同学最初不知道选择什么材料，这就可以用到我最开始推荐给大家的训练营了，我最初的听力材料全都是在训练营里面一键获取的，非常的方便，再也不用翻遍很多书籍和网站。不知道选择什么听力材料才好啦！
            </p>
            <p>
                需要注意的是，如果你的听力极差，泛听可能就不太适合了，你更需要背单词、词组，并加以精听练习。
            </p>
            <div class="subtitle"><h3 class="keyPoint">三、写作（模仿和研究真题范文）</h3></div>
            <div class="subtitle"><h3 class="keyPoint">1. 仿写</h3></div>
            <p>
                仿写就是仿照别人写的英语文章，写一篇新的文章，俗话说：“天下文章一大抄，就看会抄不会抄”，仿写也是有技巧的。

                把背诵好的范文，修改一些词汇，比如：他穿了一件白色的T恤衫，改成：他戴了一顶很酷的黑色的帽子。慢慢可以发展到：看到一篇不错的写小猫的英语文章，那我们就借鉴上面一些关键词比如 “可爱” “大大的眼睛” “跳来跳去”，仿写一篇关于小狗的英语作文。<span class="highlight3">随着仿写能力的提升，英语写作能力也会提高。</span>
            </p>
            <p>
                那么如何仿写呢？
            </p>
            <p>
                首先是我们要准备好固定的框架内容，我们拿大作文举例子，希望大家举一反三，自己进行练习：
            </p>
            <p>
                （1）辩论类：最常考的题型，各类考试出现频率在 70 % 左右，题目会给出一个或两个话题或者观点，让考生表明是支持or反对。
            </p>
            <p>
                （2）解释类：各类考试占 20 % 左右，题目会给出一个话题或者现象，让考生陈述这种现象出现的原因并且给出相应的解决方案，有一些题只要求给出原因无需提出解决方法。
            </p>
            <p>
                （3）其他：这类题目不属于以上两种，但也是一篇议论文，出现概率最低大概在 10 % 左右。因此，我们就要根据题目类型的不同准备 2- 4种写作结构。
            </p>
            <p>
                总体的写作框架可以参考以下模板：
            </p>
            <p>
                第一段（引出话题，表达观点）：一般写 3 句话，<span class="highlight3">引入背景，交代辩论话题，表达写作观点。</span>

                第二段（讲道理）：第一论据，<span class="highlight3">中心句</span>，进一步具体<span class="highlight3">阐述理由</span>，拓展，所以观点成立。

                第三段（举例子）：第二论据，中心句，举例，经典个例或排比例证，例证解释说明。

                第四段（让步）：讨论反方观点，反方论据，驳斥反方或部分同意反方观点。

                第五段（结论）：<span class="highlight3">重申写作观点，不能忽视反方观点，思想立意升华。</span>
            </p>
            <div class="subtitle"><h3 class="keyPoint">2. 改写</h3></div>
            <p>
                <span class="highlight3">改写不仅是英语写作中的重要训练手段更能够锻炼作者的写作能力，</span>改写不仅要求我们对相关的近义词有一定的了解，更需要了解改写文章的意思。
            </p>
            <p>
                【例如】：Only Tom passed the exam last week. → No one but Tom passed the exam.
            </p>
            <p>
                意思都是一样，但是句子句式全部都变了，如果多做这种练习，有助于英语写作的灵活变通。
            </p>
            <p>
                【高低级改写的区别】：高级改写和低级改写的“词 - 词”对换不同，它注重的是“词 - 句”之间的互换。
            </p>
            <div class="subtitle"><h3 class="keyPoint">3. 扩写</h3></div>
            <p>
                扩写就是根据某一句话给出的线索，或者是几个关键词，就能完成一篇英语作文，对于基础较好的同学来说英语作文不缺构思，缺的是用英语的表述，很多时候都不知道用什么词，用什么时态，所以想要提升扩写的能力，首先肚子里要有<span class="highlight3">一定量的词汇和范文，</span>只有积累了一定的写作素材，才能围绕某一个主题或者是某一句话展开写作。
            </p>
            <p>
                那么如何才能做到语言精炼、富有逻辑、内容充实的扩写呢
            </p>
            <p>
                最好的方法就是精读范文，从真题范文中汲取优秀的<span class="highlight3">语言表达和观点。</span>当然，只学习好词好句是远远不够的。范文中的观点铺陈、论据陈述更值得我们去学习。
            </p>
            <p>
                拿到一篇范文后，不要急于去看文章的具体内容。先把范文的写作框架总结提取出来，然后看范文的观点如何，是怎么进行论证的。最后再摘抄好词好句。总结完成之后，可以尝试扩写。刚开始从句子或者段落开始，最后再进阶到整篇文章的扩充整理。
            </p>
            <div class="subtitle"><h3 class="keyPoint">4、写作高分的标准</h3></div>
            <p>
                写作能拿到高分需要做到以下几点：
            </p>
            <p>
                （1）有较清晰的观点、论点和结论；
            </p>
            <p>
                （2）保持段落间的逻辑，衔接词使用恰当；
            </p>
            <p>
                （3）能使用足够的词汇和一些不常用的词汇和搭配来回答问题；
            </p>
            <p>
                （4）能综合使用简单句和复杂句，且语法正确。
            </p>
            <div class="subtitle"><h3 class="keyPoint">4、写作高分的标准</h3></div>
            <p>
                对于写作而言，我的个人经验是去看满分或者高分例文，看完你会发现写的很流畅，也没有过多复杂的句式，词汇。前期先去模仿他们的句式表达并积累一些实用词汇可以避免句式重复和“不地道表达”，这个对于大作文和小作文都很有帮助。
            </p>
            <div class="subtitle"><h3 class="keyPoint">四、阅读（反复刷近5-10年四六级真题）</h3></div>
            <p>
                <span class="highlight4">每天可花费2小时左右的时间，严格掐表做2篇左右的阅读理解，</span>然后其他时间来修改和总结。可以拿铅笔在真题上画线画圈，后面擦掉也不影响二刷，并将答案写在纸上，做错的题目在原题上用不同颜色笔标注好。一篇阅读研究透可能要花费1-2个小时，这是一个由量变到质变的过程，很煎熬，但是也必须坚持。
            </p>
            <div class="subtitle"><h3 class="keyPoint">1、做题步骤</h3></div>
            <p>
                （1）第一步：先记住开始时间
            </p>
            <p>
                用手机或者手表，记住自己开始的时间。这么做的目的不是为了让你必须10分钟做完，而是让你意识到自己做一篇阅读会花多长时间，看看自己的做题速度，并且根据时间去判断这篇阅读的难度。
            </p>
            <p>
                （2）第二步：用序号将段落标注出来
            </p>
            <p>
                阅读题干中经常会出现(Line5，para3)、(Paragraph6)这样的字眼…....有的文章段落很多，一题数一下就会很浪费时间，并且紧张还很容易数错。标注好段落之后，一下就可以找到，省时省力。
            </p>
            <p>
                （3）第三步：先看五个问题，获取关键词、预判文章内容
            </p>
            <p>
                开始做阅读理解之前，先把文章的五个问题看清楚，比如清楚这道题目考察的是单词，还是文章的释义，还是整体概括。最好拿支笔把问题和几个选项翻译出来标注在一旁，防止自己遗忘翻译从而节省时间。
            </p>
            <p>
                先看问题的目的：
            </p>
            <p>
                我们通过对问题的了解，可以<span class="highlight4">先知道这篇文章是哪个方向的，</span>比如是社会类的，还是人物介绍，还是科普类的文章，自己心里有个谱；
            </p>
            <p>
                通过分析五个问题，我们知道在<span class="highlight4">细读文章的时候应该注意哪里，</span>对有答案的句子更注意，可以<span class="highlight4">更有针对性的去读文章，省时。</span>
            </p>
            <p>
                （4）第四步：粗读文章，标记问题的位置，找出段落核心句
            </p>
            <p>
                分析完问题后，粗读一遍文章，找出每个段落的核心句，同时把问题的大致位置找出，用铅笔做勾画，标记问题的位置。

                四六级阅读与其说是考英语，不如说是考逻辑，抓出段落中心，即使无法完全读明白，也可以大幅度减少你的做题时间，英文的核心句又具有明显的特征，比如：转折之后，观点之处，特殊标点，强烈语气，如果一个段落中完全没有任何特征，那更简单，直接看首句和尾句就可以了。
            </p>
            <p>
                （5）第五步：对比选项，返回原文，根据段落逻辑和问题位置找答案
            </p>
            <p>
                此时大家容易有个思维误区，通常你的做法是，根据题干回文定位，然后根据每个选项再次回文定位，把四个选项跟原文四个定位点比对，最终战战兢兢得出答案。

                恕学长直言，请问：你读的懂吗?又或者，你有时间读懂吗?这种做题方法只有死路一条。

                时间紧任务重，咋办?直接拿段落核心句和选项比对，跟核心句含义最接近，则成为正确答案的几率就最大，省时省力，也省去了对细节干扰项的纠结，何乐而不为呢?
            </p>
